package ProblemSetSolve;

public class Line {
    //Ax + By + C
    private int A;
    private int B;
    private int C;

    public Line(int a, int b, int c) {
        A = a;
        B = b;
        C = c;
    }

    public int getA() {
        return A;
    }

    public int getB() {
        return B;
    }

    public int getC() {
        return C;
    }
}
